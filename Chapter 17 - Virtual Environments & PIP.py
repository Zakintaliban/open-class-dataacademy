# PIP

# PIP is a package manager for Python. It can be used to install, uninstall, and manage Python packages.

# To install a package using PIP, you can use the following command:

# pip install package_name

# To uninstall a package using PIP, you can use the following command:

# pip uninstall package_name

# To see a list of all of the packages that are installed in the current Python environment, you can use the following command:

# pip list

# if there's a requirements.txt file, you can install all the packages in it by using the following command:

# pip install -r requirements.txt

# to create a requirements.txt file, you can use the following command:

# pip freeze > requirements.txt

